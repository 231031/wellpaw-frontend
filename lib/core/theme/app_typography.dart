/// Shared mobile-first typography scale.
///
/// Primary sizes:
/// - `headline`: Main section/page title
/// - `subheading`: Secondary section title
/// - `body`: Normal readable content
class AppTypography {
  const AppTypography._();

  static const double headline = 24;
  static const double subheading = 18;
  static const double body = 16;

  // Supporting sizes for compact labels and helper text.
  static const double bodyCompact = 14;
  static const double caption = 12;
}
